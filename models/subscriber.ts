const mongooseDB = require('mongoose')

const subscriberSchema = new mongooseDB.Schema({
  name: {
    type: String,
    required: true
  },
  subscribedToChannel: {
    type: String,
    required: true
  },
  subscribeDate: {
    type: Date,
    required: true,
    default: Date.now
  }
})

module.exports = mongooseDB.model('Subscriber', subscriberSchema)